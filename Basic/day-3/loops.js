/**This file contains code related to loop Statements in JS */

// for loop
// print numbers from 0 to 9
for (let i = 0; i < 10; i++) {
    console.log(i);
}

// expressions are optional
let j = 0;
for (; j < 10; j++) {
    console.log(j);
}


